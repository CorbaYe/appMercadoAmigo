    private void btn_registrarseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registrarseActionPerformed
        String pass1 = new String(txt_contrasenha.getPassword());
        String pass2 = new String(txt_confirmar_contrasenha.getPassword());
        try {
            registrar_cliente(txt_usuario.getText(), txt_nombre_completo.getText(), contrasenha(pass1, pass2), txt_tarjeta_credito.getText(), fecha(), txt_email.getText(), txt_telefono.getText(), txt_direccion.getText());
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btn_registrarseActionPerformed
    private String fecha(){
        LocalDate fechaActual = LocalDate.now();
        String fecha = "" + fechaActual;
        return fecha;
    }
    
    private String contrasenha(String contrasenha1, String contrasenha2){
        if (contrasenha1.equals(contrasenha2)) {
            return contrasenha1;
        }else{
            return "No coincide";
        }
    }
    
    private void registrar_cliente(String usuario, String nombre, String clave, String tarjeta_credito, String fecha_registro, String email, String telefono, String direccion) throws SQLException{
        if (!usuario.equals("") && !nombre.equals("") && !clave.equals("") && !tarjeta_credito.equals("") && !fecha_registro.equals("") && !email.equals("") && !telefono.equals("") && !direccion.equals("")) {
            if (clave.equals("No coincide")) {
                JOptionPane.showMessageDialog(rootPane, "Las contrase침as no coinciden.");
            }else{
                JOptionPane.showMessageDialog(rootPane, clave);
                Usuario cliente = new Cliente(nombre,usuario,clave,"",fecha_registro, 
                direccion,tarjeta_credito,email, telefono); 
                cliente.registrar();
            }
        }
    }

